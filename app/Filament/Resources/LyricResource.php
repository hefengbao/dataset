<?php

namespace App\Filament\Resources;

use App\Filament\Resources\LyricResource\Pages;
use App\Filament\Resources\LyricResource\RelationManagers;
use App\Models\ChineseLyric;
use Filament\Forms;
use Filament\Forms\Form;
use Filament\Resources\Resource;
use Filament\Tables;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;

class LyricResource extends Resource
{
    protected static ?string $model = ChineseLyric::class;
    protected static ?string $modelLabel = '歌词';
    protected static ?string $pluralModelLabel = '歌词';

    protected static ?string $navigationIcon = 'heroicon-o-rectangle-stack';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Forms\Components\TextInput::make('title')->label('Title')->required()->columnSpanFull(),
                Forms\Components\TextInput::make('writer')->label('Writer')->columnSpanFull(),
                Forms\Components\Textarea::make('content')->label('Content')->required()->columnSpanFull()->rows(15),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                Tables\Columns\TextColumn::make('title')->label('Title'),
                Tables\Columns\TextColumn::make('writer')->label('Writer'),
            ])
            ->filters([
                //
            ])
            ->actions([
                Tables\Actions\EditAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\BulkActionGroup::make([
                    Tables\Actions\DeleteBulkAction::make(),
                ]),
            ]);
    }

    public static function getEloquentQuery(): Builder
    {
        return parent::getEloquentQuery()->orderByDesc('id'); // TODO: Change the autogenerated stub
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListLyrics::route('/'),
            'create' => Pages\CreateLyric::route('/create'),
            'edit' => Pages\EditLyric::route('/{record}/edit'),
        ];
    }
}
